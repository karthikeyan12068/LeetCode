class Solution {
  public:
    int equalSum(int N, vector<int> &A) {
        // code here
        for(int i=1;i<N;i++){
            A[i]+=A[i-1];
        }
        if(N==1){
            return 1;
        }
        int ind=-1;
        for(int i=1;i<N-1;i++){
            int s1=A[i-1];
            int s2=A[N-1]-A[i];
            if(s1==s2){
                ind=i+1;
                break;
            }
        }
        return ind;
    }
};


