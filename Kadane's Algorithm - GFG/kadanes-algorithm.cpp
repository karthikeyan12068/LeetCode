class Solution{
    public:
    // arr: input array
    // n: size of array
    //Function to find the sum of contiguous subarray with maximum sum.
    long long maxSubarraySum(int arr[], int n){
        long long s=0,ans=-10000001;
        
        for(int i=0;i<n;i++){
            ans=max(ans,(long long)arr[i]);
            s+=(long long)arr[i];
            if(s<0){
                s=0;
            }
            else{
                ans=max(ans,s);
            }
        }
        return ans;
        // Your code here
        
    }
};

